// ── Gemini (GEMINI.md) ────────────────────────────────────────────────

export function generateGemini(modules: ContextModule[], config: AgentCtxConfig): string {
  const filtered = getFiltered(modules, config, 'gemini');

  const parts: string[] = [
    `# ${config.project.name} — Project Context`,
    '<!-- Generated by agentctx for Gemini CLI -->',
    '',
  ];

  if (config.project.language || config.project.framework) {
    const meta: string[] = [];
    if (config.project.language) meta.push(`**Language:** ${config.project.language}`);
    if (config.project.framework) meta.push(`**Framework:** ${config.project.framework}`);
    parts.push(meta.join(' | '));
    parts.push('');
  }

  for (let i = 0; i < filtered.length; i++) {
    const mod = filtered[i];
    parts.push(`## ${mod.title}`);
    parts.push('');
    parts.push(stripHeading(mod.content));
    if (i < filtered.length - 1) {
      parts.push('');
      parts.push('---');
    }
    parts.push('');
  }

  return parts.join('\n');
}
