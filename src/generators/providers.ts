import type { ContextModule } from '../core/context.js';
import type { AgentCtxConfig } from '../core/config.js';
import { basename, extname } from 'node:path';

// ── Shared utilities ──────────────────────────────────────────────────

function shouldIncludeModule(
  mod: ContextModule,
  sections?: { include: string[]; exclude: string[] },
): boolean {
  if (!sections) return true;
  const stem = basename(mod.filename, extname(mod.filename));
  if (sections.exclude.length > 0 && sections.exclude.includes(stem)) return false;
  if (sections.include.length > 0 && !sections.include.includes(stem)) return false;
  return true;
}

function getFiltered(modules: ContextModule[], config: AgentCtxConfig, outputKey: string): ContextModule[] {
  const sections = config.outputs[outputKey]?.sections ?? { include: [], exclude: [] };
  return modules.filter(m => shouldIncludeModule(m, sections));
}

function stripHeading(content: string): string {
  return content.replace(/^#\s+.+\n?/, '').trim();
}

// ── GitHub Copilot (.github/copilot-instructions.md) ──────────────────

export function generateCopilot(modules: ContextModule[], config: AgentCtxConfig): string {
  const filtered = getFiltered(modules, config, 'copilot');

  const parts: string[] = [
    `# Copilot Instructions for ${config.project.name}`,
    '<!-- Generated by agentctx — do not edit directly -->',
    '',
  ];

  if (config.project.language) {
    parts.push(`Language: ${config.project.language}`);
  }
  if (config.project.framework) {
    parts.push(`Framework: ${config.project.framework}`);
  }
  if (config.project.language || config.project.framework) {
    parts.push('');
  }

  for (const mod of filtered) {
    parts.push(`## ${mod.title}`);
    parts.push('');
    parts.push(stripHeading(mod.content));
    parts.push('');
  }

  return parts.join('\n');
}

